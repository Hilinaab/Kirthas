            <p>







Settlement is one of the key action that led humans to begin their era of modernism. The first step that show the options for its survival, a new way of life. By having to give less to have something more.  Planting crops and raising cattle. A new way of thinking and leaving lead the human of the ancient time to settle than move from place to place in search of food and shelter this gradually grow to form the earliest urban settlement. 
The earliest known urban settlements in Ethiopia were found during the pre-Axsumite and the Axsumite periods. Long before the formation of the Aksum as a capital, there were towns and large settlements along the caravan route to the red sea port of Adulis. Followed by other urban settlements continue to immerge and grow.  
All of these urban centers supported diverse permanent population composed of merchants, artisans, celery, and their dependents. Palace, market and church were three major institutions that shaped Ethiopian towns during the preceding centuries. These institutions played political, economic and cultural roles1.  This all add up to give rise to host a comfortable living environment for the native. 
Addis Ababa, one of the main cities that developed in the country. The settlement, which at first consisted largely of tents and temporary dwelling, later interspersed with stone building, grew rapidly in size and importance1. Mainly caused by the church, the Palace and the market place. All this made the city the greatest of at the time until now. 
After being the center of the country many interventions were implemented, one of the major was the Ethio-Djibouti railway. This rail way become one of the most important for import and export of goods and item. Since the majority of the population income was based on trade; exporting what they produce and selling to the local was there day to day life. Therefore, this became a pull force for Ethiopian at every corner. In response people start to settle in the city, along and at every stop of the train. 
Thought having this as a pull factor, most of the activities are created in city center which attract people so in return populating the center and accommodating inhabitant over its limit. Hence it comes with different problem, to mention few, unaffordable and low living condition, high gap of income and expensive life. While the small village are losing their people who Is familiar to the surrounding, and who are sensitive for what the surrounding gives and how it need to be used. 
But the areas that are being abandoned have large potential that haven’t been used. Therefore, we host this website to help build a city along the main stream. To show the potential for the wide public. And to inform stakeholders and interested individuals, in respect of their field of interest to search for a potential. And implement it. So that summation of this implemented elements of various individual would create a pull factor that will lead for a populated and habitable environment. 
